def predict_ad_engagement(sample):
    result = predict(sample)       
    ctr_value = result[0][0]      

    if ctr_value < 0.015:
        engagement = "Low Engagement"
    elif ctr_value < 0.045:
        engagement = "Medium Engagement"
    else:
        engagement = "High Engagement"

    return ctr_value, engagement


